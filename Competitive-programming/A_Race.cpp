void jarvis()
{
    int a,x,y;
    cin>>a>>x>>y;
    if(x>y)swap(x,y);
    for(int i=1;i<=101;i++){
        if(i==a)continue;
        if(abs(i-x)<abs(a-x) && abs(i-y)<abs(a-y)){
            cout<<"YES"<<endl;
            return;
        }
    }
    cout<<"NO"<<endl;
}

