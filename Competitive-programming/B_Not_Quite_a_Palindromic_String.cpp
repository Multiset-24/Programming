void jarvis()
{
    int n, k;
    cin >> n >> k;
    string s;
    cin >> s;
    int z = 0, o = 0;
    for (auto c : s) {
        if (c == '0') z++;
        else o++;
    }
    int d = abs(z - o) / 2;
    if (k >= d && ((k - d) % 2 == 0)) {
        cout << "YES"<<endl;
    } else {
        cout << "NO"<<endl;
    }
}

