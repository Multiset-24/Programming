void jarvis()
{
    int n;
    cin>>n;
    string s;
    cin>>s;
    set<int>st;
    for(int i=1;i<=n;i++)st.insert(i);
    vector<int>ans(n,0);
    for(int i=n-1;i>=1;i--){
        if(s[i-1]=='<')ans[i]=*st.begin();
        else ans[i]=*st.rbegin();
        st.erase(ans[i]);
    }
    ans[0]=*st.begin();
    for(auto it:ans)cout<<it<<" ";
    cout<<endl;
}

