void jarvis()
{
    string s;
    cin>>s;
    string ans="";
    for(auto &x:s){
        if(x=='A' || x=='E' || x=='I' || x=='O' || x=='U' || x=='Y' || x=='a' || x=='e' || x=='i' || x=='o' || x=='u' || x=='y')continue;
        if(x>='A' && x<='Z')x+=32;
        ans+=x;
    }
    for(auto it:ans){
        cout<<'.'<<it;
    }
    cout<<endl;
}

