void jarvis()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    vector<int> fq(26, 0);
    for (char ch : s) fq[ch - 'a']++;
    bool ok = false;
    for (int i = 1; i <= n-2; i++) {
        if (fq[s[i] - 'a'] >= 2) {
            ok = true;
            break;
        }
    }
    cout << (ok ? "Yes" : "No") << endl;
}

