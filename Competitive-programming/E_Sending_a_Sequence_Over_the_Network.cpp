int n;
vector<int>seq;


bool dfs(int i){
    if(i<0 || i>=n)return 0;
    if(i==0)return 1;

    if(dfs(i+seq[i]+1) || dfs(i-seq[i]-1))return 1;

    return 0;
}
void jarvis()
{
    cin>>n;

    seq.assign(n,0);
    for(int i=0;i<n;i++)cin>>seq[i];

    cout<<dfs(n-1)<<endl;
}

