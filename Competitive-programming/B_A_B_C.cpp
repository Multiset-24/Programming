void jarvis()
{
    string s;
    cin>>s;

    int n=0;

    for(int i=0;i<s.size();i++){
        if(s[i]!='A')continue;
        for(int j=1;j<s.size();j++){
            if(i+j<s.size() && s[i+j]=='B'){
                if(i+2*j<s.size() && s[i+2*j]=='C')n++;
            }
        }
    }


    cout<<n<<endl;
}

