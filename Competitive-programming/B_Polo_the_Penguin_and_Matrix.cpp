void jarvis()
{
    int n,m,d;
    cin>>n>>m>>d;
    vector<int>num(n*m,0);
    for(auto &x:num)cin>>x;
    sort(num.begin(),num.end());
    int me=num.size()/2;
    int o=0;
    for(auto it:num){
        int diff=abs(it-num[me]);
        if(diff%d!=0){
            cout<<-1<<endl;
            return;
        }
        o+=(diff/d);
    }
    cout<<o<<endl;
}   

