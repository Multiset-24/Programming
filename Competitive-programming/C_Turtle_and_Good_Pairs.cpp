void jarvis()
{
    int n;
    cin>>n;
    string s;
    cin>>s;
    map<char,int>mp;
    for(auto x:s)mp[x]++;
    string ans="";
    while(mp.size()>0){
        map<char,int>t=mp;
        for(auto it:t){
            ans+=(it.first);
            mp[it.first]--;
            if(mp[it.first]==0){
                mp.erase(it.first);
            }
        }
    }
    cout<<ans<<endl;
}

