void jarvis()
{
    int a,b,c;
    cin>>a>>b>>c;
    int alpha=(c-2*b+a)/3;
    int beta=(c-2*a+b)/3;
    int x=alpha+beta;
    if(alpha<0 || beta<0){
        cout<<"NO"<<endl;
        return;
    }
    if(c-x==a+beta && c-x==b+alpha){
        cout<<"YES"<<endl;
    }
    else{
        cout<<"NO"<<endl;
    }
}

