    vector<int>ans;
    ans.push_back(num[0]);
    for(int i=1;i<n;i++){
        if(num[i]>=ans.back()){
            ans.push_back(num[i]);
        }
        else{
            ans.push_back(num[i]);
            ans.push_back(num[i]);
        }
    }
    cout<<ans.size()<<endl;
    for(auto x:ans)cout<<x<<" ";
