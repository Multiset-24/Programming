void jarvis()
{
    int n,k;
    cin>>n>>k;

    string s;
    cin>>s;

    for (int i = n-1; i >=1 && k > 0; i--) {
        if (s[i] == '1' && s[i - 1] == '0') {
            s[i-1] = '1';
            k--;
        }
    }

    int ones_count = count(s.begin(), s.end(), '1');
    cout << ones_count << endl;
}

